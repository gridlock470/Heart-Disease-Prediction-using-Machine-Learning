"""
PDF Report Generator
====================
Generates a professional medical report PDF when a doctor verifies a patient's assessment.
"""
from reportlab.lib.pagesizes import letter
from reportlab.pdfgen import canvas
from reportlab.lib.colors import HexColor
from reportlab.lib.units import inch
import os

def generate_medical_report_pdf(report, patient, doctor, reports_dir):
    """
    Generate a HeartGuard medical report PDF.

    Args:
        report: Report database object
        patient: User (patient) database object
        doctor: User (doctor) database object
        reports_dir: Absolute path to directory where PDFs are saved

    Returns:
        (filename, filepath) tuple
    """
    os.makedirs(reports_dir, exist_ok=True)

    filename = f"HeartGuard_Report_{report.id}.pdf"
    filepath = os.path.join(reports_dir, filename)

    # Create PDF
    c = canvas.Canvas(filepath, pagesize=letter)
    width, height = letter

    # Colors
    primary = HexColor("#0f172a")   # Dark background
    accent = HexColor("#f43f5e")    # Rose
    text_color = HexColor("#1e293b")

    # Header Background
    c.setFillColor(primary)
    c.rect(0, height - 1.5*inch, width, 1.5*inch, fill=1, stroke=0)

    # Header Text
    c.setFillColor(HexColor("#ffffff"))
    c.setFont("Helvetica-Bold", 24)
    c.drawString(0.5*inch, height - 0.8*inch, "HeartGuard Clinical AI")
    c.setFont("Helvetica", 12)
    c.drawString(0.5*inch, height - 1.1*inch, "Verified Medical Report")

    # Patient Information
    c.setFillColor(text_color)
    c.setFont("Helvetica-Bold", 16)
    c.drawString(0.5*inch, height - 2.0*inch, "Patient Details")

    c.setFont("Helvetica", 12)
    c.drawString(0.5*inch, height - 2.4*inch, f"Name: {patient.name}")
    pid = patient.patient_uuid[:8] if patient.patient_uuid else 'N/A'
    c.drawString(3.0*inch, height - 2.4*inch, f"Patient ID: {pid}...")
    c.drawString(0.5*inch, height - 2.7*inch, f"Age: {report.age}")
    c.drawString(3.0*inch, height - 2.7*inch, f"Sex: {'Male' if report.sex == 1 else 'Female'}")

    # AI Diagnosis Result
    c.setFont("Helvetica-Bold", 16)
    c.drawString(0.5*inch, height - 3.4*inch, "AI Diagnosis & Verification")

    # Draw a box for the result
    risk = "HIGH RISK" if report.prediction_result == 1 else "LOW RISK"
    result_color = accent if report.prediction_result == 1 else HexColor("#10b981")

    c.setFillColor(HexColor("#f8fafc"))
    c.setStrokeColor(result_color)
    c.setLineWidth(2)
    c.rect(0.5*inch, height - 4.5*inch, width - 1.0*inch, 0.8*inch, fill=1, stroke=1)

    c.setFillColor(result_color)
    c.setFont("Helvetica-Bold", 18)
    c.drawString(0.8*inch, height - 4.1*inch, f"ASSESSMENT: {risk}")

    c.setFillColor(text_color)
    c.setFont("Helvetica", 12)
    prob_str = f"Confidence: {round(report.probability * 100, 1)}%"
    c.drawString(0.8*inch, height - 4.35*inch, prob_str)

    # Medical Vitals
    c.setFont("Helvetica-Bold", 16)
    c.drawString(0.5*inch, height - 5.2*inch, "Clinical Vitals")

    vitals = [
        ("Resting Blood Pressure", f"{report.trestbps} mm Hg"),
        ("Cholesterol Level", f"{report.chol} mg/dl"),
        ("Maximum Heart Rate", f"{report.thalach} bpm"),
        ("Fasting Blood Sugar", "> 120 mg/dl" if report.fbs == 1 else "< 120 mg/dl"),
        ("Chest Pain Type", str(report.cp)),
        ("ST Depression (oldpeak)", str(report.oldpeak))
    ]

    c.setFont("Helvetica", 12)
    y = height - 5.6*inch
    for label, val in vitals:
        c.drawString(0.8*inch, y, f"{label}:")
        c.drawString(3.5*inch, y, val)
        y -= 0.3*inch

    # Doctor Sign-off
    c.setFont("Helvetica-Bold", 14)
    c.drawString(0.5*inch, y - 0.5*inch, "Doctor Verification")

    c.setFont("Helvetica", 12)
    c.drawString(0.5*inch, y - 0.8*inch, f"Verified By: Dr. {doctor.name}")
    c.drawString(0.5*inch, y - 1.1*inch, f"Specialty: {doctor.specialty or 'Cardiology'}")
    verified_date = report.verified_at.strftime('%B %d, %Y') if report.verified_at else 'N/A'
    c.drawString(0.5*inch, y - 1.4*inch, f"Date: {verified_date}")

    # Footer
    c.setFont("Helvetica-Oblique", 10)
    c.setFillColor(HexColor("#64748b"))
    c.drawString(0.5*inch, 0.5*inch, "This report is generated by HeartGuard AI and verified by a licensed medical professional.")

    c.save()
    return filename, filepath
